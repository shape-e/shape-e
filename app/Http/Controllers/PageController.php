     */
    public function store(Request $request)
    {
        $report = new Post();
        $report->url = str_replace(" ", "-", $request->get('titulo'));

        $report->titulo = $request->get('titulo');
        $report->contenido = $request->get('contenido');
        if($request->hasFile('img')){
            $file = $request->file('img');
            $name = time().$request->file('img')->getClientOriginalName();
            $destination = base_path() . '/public/portadas';
            $request->file('img')->move($destination, $name);
            $report->portada = $name;
        }

        $report->save();
        return redirect('/page/create');


    }

